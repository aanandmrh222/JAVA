public class AK_02_Variables {
    public static void main(String[] args) {
        String name = "Aanand";
        int age = 18;
        System.out.println(name + " is " + age + " years old");

        int myNumber = 420;    // initialization
        int yourNumber;        // Declaration
        yourNumber = 450;
        System.out.println("myNumber: "+myNumber);
        System.out.println("yourNumber: "+yourNumber);

        yourNumber = 45;
        System.out.println("yourNumber is updated: "+yourNumber);

        boolean pass = true;
        System.out.println(pass);

        float myFloat = 5.01f;
        System.out.println(myFloat);

        String wishes = "Good Morning";
        System.out.println(wishes);


    }
}
